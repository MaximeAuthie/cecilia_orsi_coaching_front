            Pour vivre et se développer, toute entreprise se doit aujourd’hui d’être performante.Dans quelles mesures la performance est-elle liée au stress ?<br>

            Quelle est la relation de l’entreprise face à la performance ? Pendant longtemps, la relation au travail était une relation contractuelle ; bien travailler consistait à faire ce qui était demandé.<br>

            L’agence européenne pour la sécurité et la santé au travail définit le stress comme « Un état qui survient lorsqu’il y a déséquilibre entre la perception d’une personne a des contraintes que lui impose son environnement et la perception de ses propres ressources pour y faire face. »<br>

